// Utility to generate plausible waveform bar heights
function waveform(seed: number): number[] {
  const bars: number[] = [];
  let val = seed;
  for (let i = 0; i < 60; i++) {
    val = ((val * 1103515245) + 12345) & 0x7fffffff;
    bars.push(Math.round((val % 80) + 10));
  }
  return bars;
}

